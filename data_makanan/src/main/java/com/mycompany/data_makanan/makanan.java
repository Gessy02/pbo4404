    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int selectedBaris = jTable3.getSelectedRow();
		int Baris = jTable3.convertRowIndexToModel(selectedBaris);

		String nama = tabelModel.getValueAt(Baris, 0).toString();

		int jawaban = JOptionPane.showConfirmDialog(null, "Hapus Data? " + nama);
		if (jawaban == JOptionPane.YES_OPTION){
			tabelModel.removeRow(Baris);
		}
    }//GEN-LAST:event_jButton5ActionPerformed

