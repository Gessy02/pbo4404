    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedBaris = jTable3.getSelectedRow();
		int baris = jTable3.convertRowIndexToModel (selectedBaris);
		String nama = jTable3.getValueAt(baris, 0).toString();
		String jenis = jTable3.getValueAt(baris, 1).toString();
		String rating = jTable3.getValueAt(baris, 2).toString();
		String jumlahKalori = jTable3.getValueAt(baris, 3).toString();

		String hasil = "Nama  : " + nama + "\nJenis : " + jenis 
			+  "\nRating : " + rating + "\nJumlah Kalori : " + jumlahKalori;

		JOptionPane.showMessageDialog(null, hasil);
    }//GEN-LAST:event_jButton4ActionPerformed

